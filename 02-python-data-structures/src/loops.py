# while loop
x = 0
while x < 10:
    print(x)
    x += 1  # x = x + 1

# loop over a list
l = ['a', 'b', 'c']
for item in l:
    print(item)

# loop over a number range
for x in range(10):
    print(x)

# loop over a dict
d = {
    'name': 'joe',
    'age': 50
}
for key in d:
    print(key, d[key])


